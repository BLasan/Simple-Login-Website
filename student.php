<h2>Structure of programme</h2>
<p>The Medium of conducting Lectures, Tutorials, Practical and Examinations is English. Following sub sections, will describe an overview of the methodology used for conducting the degree programme.</p>
<br>
<h3>Semester system</h3>
<p>Academic programmes of UCSC are based on a semester system with two semesters per academic year and operate on a course module basis. The Lectures, practicals, tutorials, continuous assessments and laboratory works have been spread over a period of 15 weeks. Each semester may have one week mid-semester break at the mid of the semester and a study leave of one or two weeks at the end of the semester before the examinations. 
The semester examination will be conducted within subsequent period of 2-4 weeks followed by a vacation.</p>
<br>
<h3>Hours credit system</h3>
<p>The UCSC offers two types of courses namely Academic Courses and Enhancement Courses. Academic courses provide subject knowledge and enhancement courses provide knowledge on a wide range of disciplines that are required for a holistic education.
Each course is assigned a credit value. The credit value depends on several factors including the duration of the course and its nature. A credit is equal to 15 hours of lectures or 30-45 hours of practical or an equivalent combination of lectures and practical. 80% attendance will be a requirement for a course to be satisfied before taking the corresponding exam.
The Students should complete minimum of 30 credits in each year.</p>
<br>
<h3>Course code system</h3>
<p>Each course code consists of four digits together with the prefix SCS, IS, ENH or EN to indicate whether it is a Computer Science subject, Information Systems subject or Enhancement subject. SCS and IS are academic Subjects while ENH (Computer Science) and EN (InformationSystems) represent Enhancement subjects. The first digit of each course code is the corresponding year (e.g: For a course in second year, it will be like 2xxx).
Second digit represents the revision of the subject and it will increment if the subject is revised.</p>
<h3>Enhancement Course</h3>
<p>Credits for some of the enhancement courses could also be obtained by participating to the sports conducted by Department of Physical Education or by participating to the competitions accepted by the course. 
Further information, could be obtained from the enhancement course coordinator.</p>
